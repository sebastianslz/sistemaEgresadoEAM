     public void  listar()throws Exception{
    	jCBPais.removeAllItems();
    	jCBPais.addItem("Seleccione un pais");
        List<Pais> lista=controlador.listarPais();
    	for(int i=0; i<lista.size(); i++){
			jCBPais.addItem(lista.get(i));
			
		
		}
    	      	
    }
     
     public void  listarSector()throws Exception{
    	jCBSectorLaboral.removeAllItems();
    	jCBSectorLaboral.addItem("Seleccione un pais");
        List<SectorLaboral> lista=controlador.listarSectorLaboral();
    	for(int i=0; i<lista.size(); i++){
			jCBSectorLaboral.addItem(lista.get(i));
			
		
		}
    	      	
    }
     
      public void cargarDepartamento(List<Departamento> lista) {
        jCBDepartamento.removeAllItems();
        if (lista != null) {
            jCBDepartamento.addItem("Seleccione un Departamento");
            for (int i = 0; i < lista.size(); i++) {
                jCBDepartamento.addItem(lista.get(i));
            }
        } else {
            jCBDepartamento.addItem(null);
        }
    }
      
      
       public void cargarCiudad(List<Ciudad> lista) {
        jCBCiudad.removeAllItems();
        if (lista != null) {
            jCBCiudad.addItem("Seleccione una ciudad");
            for (int i = 0; i < lista.size(); i++) {
                jCBCiudad.addItem(lista.get(i));
            }
        } else {
            jCBCiudad.addItem(null);
        }
    }
    
    
