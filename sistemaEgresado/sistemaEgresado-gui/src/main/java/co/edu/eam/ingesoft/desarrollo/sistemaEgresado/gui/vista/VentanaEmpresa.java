    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
         try{
        int codigoEmpresa= Integer.parseInt(jTFCodigoEmpresa.getText());
        String razonSocial= jTFRazonSocial.getText();
        Pais pais= (Pais) jCBPais.getSelectedItem();
        Ciudad ciudad= (Ciudad)jCBCiudad.getSelectedItem();
        String telefono= jTFTelefono.getText();
        SectorLaboral sectorL=(SectorLaboral) jCBSectorLaboral.getSelectedItem();
        String direccion=jTFDireccion.getText();
        String tipo="";
        if(jCKBPublica.isSelected()){
             tipo= jCKBPrivada.getLabel();
        }else if(jCKBPrivada.isSelected()){
            tipo=jCKBPublica.getLabel();
        }
        String pagina=jTFPaginaWeb.getText();
        Empresa empresa= new Empresa(codigoEmpresa, razonSocial, telefono, pais.getPais(), tipo, direccion, pagina, sectorL, ciudad);
        
        String NombreC=jTNombreContacto.getText();
        String TelefonoC=jTFTelefonoCon.getText();
        String cargo= jTFCargo.getText();
        String correoC=jTFCorreoContacto.getText();
        ContactoEmpresa contacto= new ContactoEmpresa(codigoEmpresa, empresa, NombreC, telefono, cargo, correoC);
        contacto.setEmpresa(empresa);
        controlador.editarEmpresa(empresa, contacto);
        }catch(Exception e1){
            e1.printStackTrace();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

