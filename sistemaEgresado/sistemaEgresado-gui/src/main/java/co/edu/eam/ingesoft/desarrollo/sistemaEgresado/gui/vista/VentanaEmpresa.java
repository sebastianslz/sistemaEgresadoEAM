    private void jCBPaisItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jCBPaisItemStateChanged
        // TODO add your handling code here:
        try {
            if (jCBPais.getSelectedIndex() == 0) {
                cargarDepartamento(null);
            } else {
                Pais facu = (Pais) jCBPais.getSelectedItem();
                if(facu == null){
                    return;
                }
                
                List<Departamento> evals = controlador.listarDepartamento(facu);
                cargarDepartamento(evals);
            }
        } catch (Exception exc) {
            
            exc.printStackTrace();
        }
    }//GEN-LAST:event_jCBPaisItemStateChanged

